export function getQuestionsByFilter(topic = null, difficulty = null) {
  return QUESTION_BANK.filter(q =>
    (!topic || q.topic === topic) &&
    (!difficulty || q.difficulty === difficulty)
  )
}

export function getRandomQuestion(topic = null, difficulty = null) {
  const pool = getQuestionsByFilter(topic, difficulty)
  if (!pool.length) return null
  return pool[Math.floor(Math.random() * pool.length)]
}